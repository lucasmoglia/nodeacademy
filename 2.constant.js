/**
 * Las constantes también son espacios en memoria en los cuales
 * podemos almacenar información!
 * A diferencia de las variables, las constantes una vez asignadas NO pueden
 * cambiar su valor. Por este motivo se llaman constantes, nunca cambian (no pueden!)
 * Para usar una constante, tenemos que:
 * - Declarar la constante
 * - Asignar la constante
 * - Usar la constante (podemos mostrar el valor)
 */

 // Declaración
 const constante;

 // Asignación
 constante = 1;

 // Utilizar la constante
 console.log(constante);