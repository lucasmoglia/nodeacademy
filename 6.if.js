/**
 * IF es una instruccion condicional.
 * La podemos usar para evaluar expresiones y hacer algo en cada caso
 */

 /**
  * Coloquialmente esto se lee como,
  * "Si dos es mayor que tres, entonces escribi '2 > 3', si no, escribi '2 < 3'."
  */
 if (2 > 3) {
     console.log('2 > 3');
 } else {
     console.log('2 < 3');
 }

 /**
  * Orden de ejecucion
  */
 let numero = 2;

 if (numero > 3) {
     console.log('es mayor que 3');
 } else {
     console.log('no es mayor que 3');
 };

 if(numero < 3) {
     console.log('es menor que 3')
 };
