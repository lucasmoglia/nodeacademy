/**
 * Desarrollar una función que reciba un número entero y retorne los cinco siguientes:
        Ej. dameLosSiguientes(5) -> 6,7,8,9,10
 */

 function dameLosSiguientes(_primerNumero){
     return (_primerNumero + 1) + ', '
        (_primerNumero + 2) + ', '
        (_primerNumero + 3) + ', '
        (_primerNumero + 4) + ', '
        (_primerNumero + 5);
 }