/**
 * Existen distintos *tipos de datos*. En NodeJS, por ejemplo, tenemos:
 * bool -> son valores lógicos, sólo acepta true o false (verdadero o falso)
 * string -> son cadenas de caracteres (uno o mas), o sea, texto. 
 *      Van encerrados entre comillas simples 'Este es un ejemplo'
 * int -> Son valores enteros
 * float -> Son valores con coma (decimales)
 */

 /**
  * Se pueden usar en variables y constantes!
  */


 // Variable de tipo entera
 let numeroEntero = 1;
 // Constante de tipo entera
 const numeroEntero = 1;
 
 // Variable de tipo bool
 let verdadero = true;
 let falso = false;
 // Constante de tipo bool
 const verdadero = true;
 const falso = false;

 // Variable de tipo string (texto)
 let texto = 'Hola! :)';
 // Constante de tipo string (texto)
 const texto = 'Hola! :)';

 // Variable de tipo decimal
 let numeroEntero = 1.18;
 // Constante de tipo decimal
 const numeroEntero = 1.18;